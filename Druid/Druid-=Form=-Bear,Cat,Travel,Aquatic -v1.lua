Выходит из любой формы или принимает фору если не в форме.
-=Bear Form=-
/script local num,ix=GetNumShapeshiftForms()for ix=1,num do if strfind(GetShapeshiftFormInfo(ix),"WispSplode")then CastShapeshiftForm(ix)num=0;end;end;for ix=1,num do if strfind(GetShapeshiftFormInfo(ix),"BearForm")then CastShapeshiftForm(ix)end;end;
-=-
/script local num,ix=GetNumShapeshiftForms()
for ix=1,num do 
	if strfind(GetShapeshiftFormInfo(ix),"WispSplode")then 
		CastShapeshiftForm(ix)num=0;
	end;
end;
for ix=1,num do 
	if strfind(GetShapeshiftFormInfo(ix),"BearForm")then 
		CastShapeshiftForm(ix)
	end;
end;

