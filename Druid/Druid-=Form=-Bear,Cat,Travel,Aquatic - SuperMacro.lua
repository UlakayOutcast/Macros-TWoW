/script if buffed("Bear Form")~="buff" and buffed("Cat Form")~="buff" and buffed("Travel Form")~="buff" and buffed("Aquatic Form")~="buff" and buffed("Dire Bear Form")~="buff" and buffed("Moonkin Form")~="buff" then SpellStopCasting();CastSpellByName("Bear Form");else if buffed("Bear Form")=="buff" then CastSpellByName("Bear Form");end;if buffed("Dire Bear Form")=="buff" then CastSpellByName("Dire Bear Form");end;if buffed("Cat Form")=="buff" then CastSpellByName("Cat Form");end;if buffed("Travel Form")=="buff" then CastSpellByName("Travel Form");end;if buffed("Aquatic Form")=="buff" then CastSpellByName("Aquatic Form");end;if buffed("Moonkin Form")=="buff" then CastSpellByName("Moonkin Form");end;end;
