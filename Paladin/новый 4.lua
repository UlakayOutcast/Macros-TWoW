-=Paladin multi-attack=-
Использует различные умения паладина (Holy Strike, 5 видов Seal, Judgement, Exorcism, Hammer of Wrath, Holy Shock, Shield\Protection, Consecration+ShiftKey,) в зависимости от ситуации и выбранной ауры.
Содержит почти 5000 символов, поэтому необходимо дополнение на подобии SuperMacro.
1й вариант не привязан к ауре.
2й и 3й варианта слегка отличающиеся по действиям.